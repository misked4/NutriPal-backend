SELECT TOP 1 [korisnici].[id]
      ,[Ime]
      ,[Prezime]
      ,[Email]
      ,[Lozinka]
      ,[Datum_rodjenja]
      ,[Uloga]
      ,[Telefon]
      ,[Adresa]
      ,[Slika]
      ,[Dodatne_info_Id]
      ,[Pol]
      ,[Cloudinary_public_id]
      ,[KreatorId]
      ,[Visina]
      ,[Tezina]
      ,[PotrosnjaKalorija]
      ,[DijetaId]
      ,[Cilj_ishrane]
      ,[Prvi_izvestaj]
      ,[BMR]
      ,[TEE]
      ,[BMI]
  FROM [nutripal].[dbo].[korisnici]
  INNER JOIN dodatne_informacije on korisnici.Dodatne_info_Id = dodatne_informacije.id
  WHERE [Email] = @Email AND [Lozinka] = @Password